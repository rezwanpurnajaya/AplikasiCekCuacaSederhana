    private void comboFavoritesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_comboFavoritesActionPerformed
     String sel = (String) comboFavorites.getSelectedItem();
    if (sel != null && !sel.equals("Pilih Favorit...")) {
        txtCity.setText(sel);
    }
    
    }//GEN-LAST:event_comboFavoritesActionPerformed
// Method untuk memuat kota favorit dari file ke combo box
private void muatFavorit() {
    try {
        // Ambil daftar favorit dari LogikaCekCuacaSederhana
        List<String> favs = LogikaCekCuacaSederhana.muatFavorit();

        // Tambahkan ke combo box
        for (String f : favs) {
            comboFavorites.addItem(f);
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Gagal memuat daftar favorit: " + e.getMessage());
    }
}


