    private void btnCheckActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCheckActionPerformed
        String city = txtCity.getText().trim();
    if (city.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Masukkan nama kota!");
        return;
    }

    try {
        WeatherData data = LogikaCekCuacaSederhana.ambilDataCuaca(city);
        lblCity.setText("Kota: " + data.getCity());
        lblTemp.setText(String.format("Suhu: %.1f °C", data.getTempC()));
        lblDesc.setText("Kondisi: " + data.getDescription());
        lblTime.setText("Waktu: " + LogikaCekCuacaSederhana.formatWaktu(data.getTimestamp()));

        // Ambil ikon dari internet
        Image img = ImageIO.read(new URL(LogikaCekCuacaSederhana.urlIkon(data.getIconCode())));
        lblIcon.setIcon(new ImageIcon(img.getScaledInstance(80, 80, Image.SCALE_SMOOTH)));

        // Tambahkan ke tabel
        DefaultTableModel model = (DefaultTableModel) tblWeather.getModel();
        model.addRow(new Object[]{
            data.getCity(),
            String.format("%.1f", data.getTempC()),
            data.getDescription(),
            LogikaCekCuacaSederhana.formatWaktu(data.getTimestamp())
        });

    } catch (Exception ex) {
        JOptionPane.showMessageDialog(this, "Gagal mengambil data cuaca: " + ex.getMessage());
    }
    }//GEN-LAST:event_btnCheckActionPerformed

    private void btnAddFavActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddFavActionPerformed
     String city = txtCity.getText().trim();
    if (city.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Masukkan nama kota terlebih dahulu!");
        return;
    }

    comboFavorites.addItem(city);

    // Simpan semua favorit ke file
    List<String> favorit = new ArrayList<>();
    for (int i = 0; i < comboFavorites.getItemCount(); i++) {
        String item = comboFavorites.getItemAt(i);
        if (!item.equals("Pilih Favorit...")) {
            favorit.add(item);
        }
    }

    LogikaCekCuacaSederhana.simpanFavorit(favorit);
    JOptionPane.showMessageDialog(this, "Kota '" + city + "' disimpan ke daftar favorit!");

    }//GEN-LAST:event_btnAddFavActionPerformed

    private void btnSaveCSVActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveCSVActionPerformed
         JFileChooser fc = new JFileChooser();
    if (fc.showSaveDialog(this) == JFileChooser.APPROVE_OPTION) {
        File file = fc.getSelectedFile();
        try {
            DefaultTableModel model = (DefaultTableModel) tblWeather.getModel();
            LogikaCekCuacaSederhana.simpanCSV(file, model);
            JOptionPane.showMessageDialog(this, "Data berhasil disimpan ke: " + file.getAbsolutePath());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal menyimpan: " + e.getMessage());
        }
    }

    }//GEN-LAST:event_btnSaveCSVActionPerformed

